package com.sist;

public class Ex03 {

	public static void main(String[] args) {

		/*
		 * 실수형 : 소수점이 존재함. float < double(기본형)
		 * float 자료형 선언시에는 반드시 실수 뒤에 f를 붙여주어야 한다(생략하면 오류)
		 * f를 안 붙이면 오류가 발생하는 이유 : 실수형 기본자료형인 double로 인식
		 * 실수형 형변환은 거의 자료형의 손실이 없다. 
		 */
		
		float fNum = 12.457f;
		
		System.out.println("fNum ==> " + fNum);
		
		
	}

}
